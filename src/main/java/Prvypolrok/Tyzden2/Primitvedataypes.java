    public static int Tomas(int[] cisla) {

        int najvacsie = 0;
        for (int i = 0; i < cisla.length; i++) {

            if (cisla[i] > najvacsie) {
                najvacsie = cisla[i];
            }
        }

        return najvacsie;


    }

    public static String Neparne(int [] l) {
        int parnec = 0;
        int neparnec  = 0;

        for (int i = 0; i < l.length; i++){
            if(l[i]%2!=0){

                neparnec++;

            }else {

               parnec++;

            }

        }
        return "Parne cisla :" + parnec + " neparne cisla :" + neparnec;
    }
